public class ConditionalDemo{
	
	public static void main(String args[]){
		String str = "Jaipur";
		
		if(str == "Jaipur"){
			System.out.println("This is pink city");
		}else if(str == "Jodhpur"){
			System.out.println("This is Sun city");
		}else{
			System.out.println("Not a pink city");
		}
	}	
}
